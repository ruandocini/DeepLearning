#There are 5 key components of a genetic algo,
# Chromosomes, Generation, Selection, Crossover, Mutation
# Chromosomes: represent a potential solution to the problem
# Gereration: comprises multiple chromosomes, we get a new generation after each iteration
# Selection: based on "fitness", compare two chromosomes and select the better one to pass on the next generation
# Crossover: process of mating to form a better solution
# Mutation: Random changes in some solutions to mimic mutation

